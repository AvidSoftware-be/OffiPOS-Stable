DROP VIEW IF EXISTS vwItemTotals;
CREATE VIEW vwItemTotals
AS
SELECT
  count(ticketLine.productId) as qty,
  ticketLine.productId,
  product.name as productName,
  sum(ticketLine.price) as total,
  product_group.name as productGroupName,
  product_group.id as groupId
FROM
  product
  INNER JOIN ticketLine ON (product.id = ticketLine.productId)
  INNER JOIN product_group ON (product.groupId = product_group.id)
GROUP BY
  ticketLine.productId
ORDER BY
  product_group.id,
  ticketLine.productId;
